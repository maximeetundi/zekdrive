export function useI18n() {
  const { lang } = useTheme()

  const t = computed(() => {
    return translations[lang.value] || translations.fr
  })

  function translatePath(text: string, args: Record<string, string> = {}) {
    let result = (t.value as any)[text] || text
    for (const [key, val] of Object.entries(args)) {
      result = result.replace(`{${key}}`, val)
    }
    return result
  }

  return {
    t: translatePath,
    lang
  }
}
